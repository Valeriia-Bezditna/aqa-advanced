//------for
console.log("Multiplication by 7");
const number = 7;

for (let i = 1; i <= 9; i++) {

	console.log(number + " " + "X" + " " + i + " " + '=' + " " + number * i);

}

//----While

console.log("Multiplication by 5");
const number1 = 5;
let a = 1;
while (a <= 9) {
	console.log(number1 + " " + "X" + " " + a + " " + '=' + " " + number1 * a);
	a++;
}

